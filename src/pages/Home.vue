      getMenuList(){                           //---------------------获取列表数据
        let para = {
          account:getCookie('user')
        };
        getMenuData(para).then((res) => {
          let data = res.data;
          let arr=[];
          for (let i =0;i<data.length;i++){
            let obj={};
            if (data[i].pId==0){
              obj.name=data[i].resName;
              arr.push(obj);
            }
            obj.children=[];
            for (let j=0;j<data.length;j++){
              let obj2={};
              if (data[j].pId==data[i].resId) {
                obj2.name=data[j].resName;
                obj2.path=data[j].url;
                obj.children.push(obj2);
              }
            }
          }
          this.menusData = arr;
        });
      },
			//退出登录
			logout: function () {
				var _this = this;
				this.$confirm('确认退出吗?', '提示', {
					//type: 'warning'
				}).then(() => {
          delCookie('user')
				//	sessionStorage.removeItem('user');
					_this.$router.push('/');
				}).catch(() => {
				});
			}
		},
     mounted(){
